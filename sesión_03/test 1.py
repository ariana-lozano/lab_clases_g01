"""Listas pop() : Quita un elemento de una posición dada"""

paises = ["Perú", "Canadá", "Argentina","México","España"]
print("Mi lista inicia: {}".format(paises))

paises.pop()
print("Mi lista actualizada1 es: {}".format(paises))

# Para nombrar un dato de la lista
print("paises[2]: {}".format(paises[2]))

paises.pop(2)
print("Mi lista actualizada2 es: {}".format(paises))

paises.pop(2)
print("Mi lista actualizada3 es: {}".format(paises))

